def build_html_with_svgs():
    for rel_path, data in CUSTOM_HTML_BLOCKS.items():
        html_file = f"html/{rel_path}.html"
        if not os.path.exists(html_file):
            print(f"Skipping {html_file}, doesn't exist yet.")
            continue
            
        with open(html_file, "r", encoding="utf-8") as f:
            content = f.read()

        # Simple replacement strategy for these files: 
        # Replace the <h2> section with the massive custom block
        
        if "Thermal_Mass_Cooking" in html_file:
            content = re.sub(r'<h2>2\. Zero-Tech Thermodynamics: The Rocket Stove</h2>.*?<hr>', data + '\n        <hr>', content, flags=re.DOTALL)
        elif "Ceramic_Synthesis" in html_file:
            content = re.sub(r'<h2>3\. Forming and Zero-Tech Firing \(The Pit Kiln\)</h2>.*?<hr>', data + '\n        <hr>', content, flags=re.DOTALL)
        elif "Cordage_and_Rope" in html_file:
            content = re.sub(r'<h2>3\. The Reverse Wrap \(The Splicing Technique\)</h2>.*?<hr>', data + '\n        <hr>', content, flags=re.DOTALL)

        with open(html_file, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"Injected SVG and HTML details into {html_file}")

